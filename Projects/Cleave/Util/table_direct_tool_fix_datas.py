# - 基于线性回归对单列数据修复
def FixByLearnRegression(
        X : Union[ndarray, List[Number]],
        Y : Union[ndarray, List[Number]],
        *,
        fit_intercept=True,
        _positive=False,
        copy_X=True,
        n_jobs=None
):
    from sklearn.linear_model import LinearRegression
    model = LinearRegression(fit_intercept=fit_intercept, positive=_positive, copy_X=copy_X, n_jobs=n_jobs)
    model : LinearRegression = model.fit(x=X, y=Y)
    

